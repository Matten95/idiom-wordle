const GENERIC_HINTS = new Set([
  '态度','行动','口语','职场','做法','好友','泥坑','方式','情况','状态','关系','变化',
  '结果','表现','感觉','问题','事情','道理','生活','日常','场面','说法','处理','选择',
])

function hasAnswerChar(idiomText, hint) {
  return Array.from(idiomText).some(char => hint.includes(char))
}

function getEntryHints(entry) {
  return Array.isArray(entry) ? entry : entry.hints
}

function getDifficulty(idiomText, entry) {
  if (Array.isArray(entry)) return IDIOM_LEVELS[idiomText] || 2
  return entry.difficulty || 2
}

function isDefaultEligible(idiomText, entry, defaultMaxDifficulty) {
  if (entry && !Array.isArray(entry) && entry.defaultEligible === false) return false
  if (entry && !Array.isArray(entry) && entry.defaultEligible === true) return true
  return getDifficulty(idiomText, entry) <= defaultMaxDifficulty
}

function validateEntry(idiomText, entry) {
  if (idiomText.length !== 4) throw new Error(idiomText + ' 不是四字成语')
  const hints = getEntryHints(entry)
  if (!Array.isArray(hints) || hints.length !== 5) throw new Error(idiomText + ' 必须有 5 条提示')
  const seen = new Set()
  hints.forEach(hint => {
    if (typeof hint !== 'string' || hint.length < 2 || hint.length > 4) {
      throw new Error(idiomText + ' 提示长度需为 2-4 字: ' + hint)
    }
    if (seen.has(hint)) throw new Error(idiomText + ' 存在重复提示: ' + hint)
    if (GENERIC_HINTS.has(hint)) throw new Error(idiomText + ' 使用泛化安全词提示: ' + hint)
    if (hasAnswerChar(idiomText, hint)) throw new Error(idiomText + ' 的提示包含答案字: ' + hint)
    seen.add(hint)
  })
  if (!Array.isArray(entry)) {
    if (!entry.source) throw new Error(idiomText + ' 缺少 source')
    if (!entry.storyHook) throw new Error(idiomText + ' 缺少 storyHook')
    if (entry.difficulty < 1 || entry.difficulty > 4) throw new Error(idiomText + ' difficulty 不合法')
  }
}

