    if (hint.length >= 2 && hint.length <= 4 && !used.has(hint) && !hasAnswerChar(idiomText, hint)) {
      used.add(hint)
      return hint
    }
    const start = (idiomText.charCodeAt(index % idiomText.length) + index * 7) % SAFE_HINTS.length
